# 2. Второе слово — буквы с 10 по 13 включительно (индексы 9–12, так как 10‑я буква — индекс 9, а срез не включает последний индекс)
word2 = secret_message[1][9:13]

# 3. Третье слово — буквы с 6 по 15 включительно, через одну (индексы 5–14, шаг 2)
